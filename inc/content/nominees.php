<section id="guidelines" class="nomination-guidelines clearfix">
    <h2 class="section-header"><span class="text">Nomination Guidelines</span></h2>
    <div class="container wrapper text-wrapper">
        <h3>Eligibility for Nomination</h3>
        <ul>
            <li>Must have earned a Stony Brook University undergraduate or graduate degree</li>
            <li>Must be 40 years of age or younger during the calendar year 2015</li>
            <li>Must submit a professional photograph that will be used by Stony Brook University</li> 
        </ul>
        <p><em>*Past nominees will remain in consideration for future years and can receive repeat recognition</em></p>
        <h3>Criteria for Nomination</h3>
        <ul>
            <li>Nominees have demonstrated achievements in business, research, technology, healthcare, the arts, leadership, community, educational, entrepreneurial and/or philanthropic endeavors</li>
            <li>Nominees exemplify the spirit of Stony Brook, including commitment, passion, leadership and the pursuit of excellence</li>
        </ul>
        <h3>Nomination Process</h3>
        <p>Nominations can be made by anyone who knows the nominee and has access to the required supporting information (including but not limited to a colleague, client, employer, family member, peer or member of the University faculty and staff). Self-nominations are accepted, but a letter of recommendation from a third party must be sent to alumni@stonybrook.edu</p>
        <p>Awards are determined by the University's 40 under Forty Awards Committee, comprised of representatives from the University's senior administration, alumni leadership, faculty and deans</p>
        <p><strong>Details regarding the nomination process for the 2015 40 under Forty awards will be coming soon</strong></p>
        <p><em>The 40 Under Forty Alumni Awards is sponsored by the Stony Brook University Alumni Association, a separate 501 (c) (3) non profit organization.</em></p>
    </div>
